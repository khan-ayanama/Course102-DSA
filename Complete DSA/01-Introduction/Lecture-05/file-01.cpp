#include <iostream>
using namespace std;
int main(){
    // Bitwise AND
    cout<<"2 & 3 is "<<(2&3)<<endl;

    // Bitwise OR
    cout<<"2 | 3 is "<<(2|3)<<endl;

    // Bitwise XOR
    cout<<"2 ^ 3 is "<<(2^3)<<endl;

    // Bitwise NOT
    cout<<"~3 is "<<(~3)<<endl;

    // Left Shift (<<)
    cout<<"5 << 1 is "<<(5<<1)<<endl;

    // Right Shift (<<)
    cout<<"5 >> 1 is "<<(5>>1)<<endl;
}